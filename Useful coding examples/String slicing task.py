alphabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZ"

print(f"The first five letters of the alphabet are {alphabet[0:5]}")
print(f"The last five letters of the alphabet are {alphabet[21:26]}")
print(f"The 10th to 16th letters of the alphabet are {alphabet[11:16]}")
print(f"The alphabet reversed is {alphabet[::-1]}")
x = alphabet.find("J")
print(f"The position of J in the alphabet is {x+1}")
